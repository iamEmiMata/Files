	echo 'Num1 = 2, Num2 = 4 <br>Suma ', $resultado1;
	echo '<br>Resta ', $resultado2;
	echo '<br>Dividir ', $resultado3;
	echo '<br>Multiplicar ', $resultado4;
	echo '<br>Resto ', $resultado5;
	echo '<br>Potencia ', $resultado6;
